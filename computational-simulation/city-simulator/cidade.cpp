char cidadeSimulada[100][100];

// Parâmetros globais
int taxaResidencia = 6; // Probabilidade de célula vazia virar uma residência
int taxaComercio = 4;   // Probabilidade de uma célula vazia virar um comércio
int taxaIndustria = 1;   // Probabilidade de uma célula vazia virar uma indústria
int tamanhoMaximo, tamanhoSimulacao; // Declaração de variáveis para controle do tamanho da cidade. Uma delas será atribuída com o tamanho máximo que a cidade poderá atingir, e a outra será incrementada a cada alteração de tamanho na cidade

void copiarArray() {
   /*
   Função para copiar a cidade original para a variável sob a qual a simulação será executada
   */
   for(int i = 0; i < 100; i++) {
      for(int j = 0; j < 100; j++) {
         cidadeSimulada[i][j] = cidade[i][j];
      }
   }
}

template <size_t linhas, size_t colunas>
int calculaCidade(char (&array)[linhas][colunas]) {
   /*
   função que retorna o tamanho da cidade passada como parâmetro. Foi construída assim para servir tanto para a cidade original como para a cidade simulada
   */
    int count = 0;
    for (size_t i = 0; i < linhas; ++i) {
        for (size_t j = 0; j < colunas; ++j) {
            if (array[i][j] != 0) {
                ++count;
            }
        }
    }
    return count;
}

//-----------------------------------------------------------------------
void Pause (int milliseconds) {
    struct timespec ts;
    ts.tv_sec = 0;//milliseconds / 1000;
    ts.tv_nsec = (milliseconds % 1000) * 1000000;
    nanosleep(&ts, NULL);
}


